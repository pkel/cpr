mountaincar = QuickMDP(
    function (s, a, rng)        
        x, v = s
        vp = clamp(v + a*0.001 + cos(3*x)*-0.0025, -0.07, 0.07)
        xp = x + vp
        if xp > 0.5
            r = 100.0
        else
            r = -1.0
        end
        return (sp=(xp, vp), r=r)
    end,
    actions = [-1., 0., 1.],
    initialstate = Deterministic((-0.5, 0.0)),
    discount = 0.95,
    isterminal = s -> s[1] > 0.5
)

# ╔═╡ b2da30f8-ddab-4b24-9563-eebd2a3a5ec8
begin
	using MCTS
	
	solver = MCTSSolver(n_iterations=1000, depth=5, exploration_constant=25.0) 
	planner = solve(solver, fc16ssz)

	for i in 1:10
		rew = 0
		for (s, a, r) in POMDPTools.Simulators.stepthrough(fc16ssz, planner, "s,a,r", max_steps=1000)
			rew += r
		end
		println("acc $rew")
	end
end

