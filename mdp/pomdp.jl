# ╔═╡ 47f77643-e991-4e40-8132-929108e7ffb7
begin
	mfl = 10 # max fork length
	gamma = 0.5
	alpha = 1/4
	
	@enum ACTION wait match override adopt
	@enum FORK irrelevant relevant active

	# this describes Bitcoin Selfish Mining as generative model. I did this because I could not figure out how to calculate new state and reward in one go, using the 'transitions' interface. In the meantime I had an idea: just put the reward in the new state, then make the 'reward' function extract the reward from the state, ignoring the last state. Also, the full POMDP interface might have a better method for doing this.
	fc16ssz = QuickMDP(
		function (s, act, rng)
			a, h, fork = s
			if act == adopt
				if rand(rng, Bernoulli(alpha))
					return (sp=(1, 0, irrelevant), r=0, info=(p=h))
				else
					return (sp=(0, 1, irrelevant), r=0, info=(p=h))
				end
			elseif act == override
				if rand(rng, Bernoulli(alpha))
					return (sp=(a - h, 0, irrelevant), r=h+1, info=(p=h+1))
				else
					return (sp=(a - h - 1, 1, relevant), r=h+1, info=(p=h+1))
				end
			elseif act == match
				# equals active wait
				if rand(rng, Bernoulli(alpha))
					return (sp=(a + 1, h, active), r=0, info=(p=0))
				else
					if rand(rng, Bernoulli(gamma))
						return (sp=(a - h, 1, relevant), r=h, info=(p=h))
					else
						return (sp=(a, h+1, relevant), r=0, info=(p=0))
					end
				end
			elseif act == wait
				if fork == active
					if rand(rng, Bernoulli(alpha))
						return (sp=(a + 1, h, active), r=0, info=(p=0))
					else
						if rand(rng, Bernoulli(gamma))
							return (sp=(a - h, 1, relevant), r=h, info=(p=h))
						else
							return (sp=(a, h+1, relevant), r=0, info=(p=0))
						end
					end
				else
					if rand(rng, Bernoulli(alpha))
						return(sp=(a + 1, h, irrelevant), r=0, info=(p=0))
					else
						return(sp=(a, h+1, relevant), r=0, info=(p=0))
					end
				end
			else
				@assert false
			end
		end,
		actiontype = ACTION,
		actions = function (s)
			a, h, fork = s
			act = []
			if a >= 0 && h >= 0
				push!(act, adopt)
			end
			if mfl < 1 || (a < mfl && h < mfl)
				push!(act, wait)
			end
			if a > h
				push!(act, override)
			end
			if a >= h
				push!(act, match)
			end
			return act
		end,
		initialstate = SparseCat([(1, 0, irrelevant), (0, 1, irrelevant)], [alpha, 1-alpha])
	)
end

# ╔═╡ bcb3b50e-5077-4d2d-a089-b1436d86ea0f
begin
	using POMDPTools
	policy = POMDPTools.Policies.RandomPolicy(fc16ssz)
	
	rew1 = 0
	for (s, a, r) in POMDPTools.Simulators.stepthrough(fc16ssz, policy, "s,a,r", max_steps=1000)
		global rew1 += r
	end
	println("acc $rew1")
end

